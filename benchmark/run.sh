#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
CORPUS="$SCRIPT_DIR/corpus"
RESULTS="$SCRIPT_DIR/results"
MARKIT_OUT="$RESULTS/markit"
MARKITDOWN_OUT="$RESULTS/markitdown"

mkdir -p "$MARKIT_OUT" "$MARKITDOWN_OUT"

# Check dependencies
command -v markitdown >/dev/null 2>&1 || { echo "❌ Install markitdown: pip install 'markitdown[all]'"; exit 1; }
command -v hyperfine >/dev/null 2>&1 || { echo "❌ Install hyperfine: brew install hyperfine"; exit 1; }
command -v bun >/dev/null 2>&1 || { echo "❌ Install bun: curl -fsSL https://bun.sh/install | bash"; exit 1; }

files=("$CORPUS"/*)
if [ ${#files[@]} -eq 0 ]; then
  echo "❌ No files in $CORPUS"
  echo "   Run: ./benchmark/setup-corpus.sh"
  exit 1
fi

# ── Quality: generate outputs ──────────────────────────────────────

echo "=== Generating outputs ==="
for f in "${files[@]}"; do
  name=$(basename "$f")
  base="${name%.*}"
  echo "  → $name"
  bun run --cwd "$SCRIPT_DIR/.." dev -- "$f" > "$MARKIT_OUT/${base}.md" 2>/dev/null || echo "    ⚠️  markit failed"
  markitdown "$f" > "$MARKITDOWN_OUT/${base}.md" 2>/dev/null || echo "    ⚠️  markitdown failed"
done

# ── Speed: hyperfine ───────────────────────────────────────────────

echo ""
echo "=== Running speed benchmarks ==="

speed_json="$RESULTS/speed.json"
summary="$RESULTS/summary.md"

# Collect all hyperfine results into one JSON array
echo "[" > "$speed_json"
first=true
tmpjson=$(mktemp)

for f in "${files[@]}"; do
  name=$(basename "$f")
  echo "  → $name"

  hyperfine --warmup 1 --min-runs 3 -i --export-json "$tmpjson" \
    --command-name "markit" "bun run --cwd '$SCRIPT_DIR/..' dev -- '$f' > /dev/null 2>&1" \
    --command-name "markitdown" "markitdown '$f' > /dev/null 2>&1" \
    > /dev/null 2>&1

  if [ "$first" = true ]; then
    first=false
  else
    echo "," >> "$speed_json"
  fi

  # Wrap with filename metadata
  printf '{"file": "%s", "results": %s}' "$name" "$(cat "$tmpjson")" >> "$speed_json"
done

rm -f "$tmpjson"
echo "]" >> "$speed_json"

# ── Summary table ──────────────────────────────────────────────────

echo ""
echo "=== Generating summary ==="

cat > "$summary" << 'HEADER'
# Benchmark: markit vs Microsoft markitdown

Environment: Bun + TypeScript vs Python
Date: $(date +%Y-%m-%d)

## Speed

| File | Size | markit | markitdown | Speedup |
|------|------|--------|------------|---------|
HEADER

# Replace the date placeholder
sed -i '' "s/\$(date +%Y-%m-%d)/$(date +%Y-%m-%d)/" "$summary"

for f in "${files[@]}"; do
  name=$(basename "$f")
  base="${name%.*}"
  size=$(du -h "$f" | cut -f1 | tr -d ' ')

  # Extract mean times from hyperfine JSON
  markit_ms=$(cat "$speed_json" | python3 -c "
import json, sys
data = json.load(sys.stdin)
for entry in data:
    if entry['file'] == '$name':
        for r in entry['results']['results']:
            if r['command'] == 'markit':
                print(f\"{r['mean']*1000:.0f}\")
" 2>/dev/null || echo "?")

  markitdown_ms=$(cat "$speed_json" | python3 -c "
import json, sys
data = json.load(sys.stdin)
for entry in data:
    if entry['file'] == '$name':
        for r in entry['results']['results']:
            if r['command'] == 'markitdown':
                print(f\"{r['mean']*1000:.0f}\")
" 2>/dev/null || echo "?")

  if [ "$markit_ms" != "?" ] && [ "$markitdown_ms" != "?" ] && [ "$markit_ms" -gt 0 ] 2>/dev/null; then
    speedup=$(python3 -c "print(f'{$markitdown_ms/$markit_ms:.1f}x')")
  else
    speedup="?"
  fi

  echo "| $name | $size | ${markit_ms}ms | ${markitdown_ms}ms | **$speedup** |" >> "$summary"
done

# Quality section
cat >> "$summary" << 'QUALITY'

## Quality

| File | markit (bytes) | markitdown (bytes) | Diff |
|------|---------------|-------------------|------|
QUALITY

for f in "$MARKIT_OUT"/*.md; do
  base=$(basename "$f")
  other="$MARKITDOWN_OUT/$base"
  [ -f "$other" ] || continue

  mb=$(wc -c < "$f" | tr -d ' ')
  db=$(wc -c < "$other" | tr -d ' ')

  if diff -q "$f" "$other" > /dev/null 2>&1; then
    verdict="Identical"
  else
    adds=$(diff "$f" "$other" 2>/dev/null | grep -c '^>' || true)
    removes=$(diff "$f" "$other" 2>/dev/null | grep -c '^<' || true)
    verdict="+$adds/-$removes lines"
  fi

  echo "| $base | $mb | $db | $verdict |" >> "$summary"
done

echo "" >> "$summary"
echo "_Generated by \`benchmark/run.sh\`_" >> "$summary"

# ── Print summary ──────────────────────────────────────────────────

echo ""
cat "$summary"
echo ""
echo "Full results: $RESULTS/"
echo "  summary.md    — this table"
echo "  speed.json    — raw hyperfine data"
echo "  markit/       — markit outputs"
echo "  markitdown/   — markitdown outputs"
